/* Convert `len`-byte long big number, stored in big-endian in `buf`, to a
 * hexadecimal string (beg-endian as well).
 */
char *stringify_bignum(void *buf, int len)
{
        char *str = malloc(2*len+1);
        memset(str, 0, 2*len+1);

        uint8_t *num = buf;
        for (int i = 0; i < len; i++)
                snprintf(&str[2*i], 3, "%02hhx", num[i]);

        return str;
}

/* Use GMP's Miller-Rabin primality test functionality.
 */
int is_prime(char *str_bignum)
{
        mpz_t num;
        mpz_init(num);
        mpz_set_str(num, str_bignum, 16);
        int primality = mpz_probab_prime_p(num, 30);
        mpz_clear(num);
        return primality;
}

